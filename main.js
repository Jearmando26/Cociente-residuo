// variables

let n1, n2, cociente, residuo;

// Datos

n1 = Number(prompt('Ingrese un numero'));
n2 = Number(prompt('Ingrese un numero'));

// Proceso

cociente = n1 / n2;
residuo = n1 % n2;

// Salid de datos

document.write('Cociente:' + cociente + '<br>');
document.write('Residuo:' + residuo + '<br>');